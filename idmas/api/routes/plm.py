#     - 回写流程: 幂等Key检查 → SOA/REST写入 → 记录幂等Key
#     - 失败重试: 指数退避 30s,60s,120s,300s,600s (共5次)
#     - 5次全败 → 进入死信队列 → 告警
#
#   POST /api/v1/plm/webhook        PLM Webhook接收
#     - 验证HMAC签名 + IP白名单
#     - 处理PLM推送的文档变更通知
